    {


        $configuration = new Configuration();
        $configuration->json = $request->json;
        $configuration->mac_address = $request->mac_address;
        $configuration->save();

        $response = ApiHelpers::apiResponse(false, 200, '', $configuration);

        return response()->json($response, 200);
    }

